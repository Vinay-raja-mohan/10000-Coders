"Comparison Operators in Python"
a = "ab"
b = 'ba'
print(a == b)        # False
print("---------------------------------------------------------------------")

"Logical Operators"

# and
v1 = 100
v2 = 200
result = v1<v2 and v1!=v2
print(result)          # True
print("----------------")

# or
n=10
if n%2==0 or n%3==0:
    print("Divisible by 2 or 3")   # Divisible by 2 or 3
print("----------------")

# not
a = 100
b = not a>200
print(b)               # True
print("---------------------------------------------------------------------")



"Bitwise Operators"
# &
b1 = 5        # 0101
b2 = 3        # 0011
print(b1 & b2)     # 1  (0001)
print("----------------")

b3 = 8      # 1000
b4 = 6      # 0110
print(b3&b4)    # 0  (0000))
print("----------------")

# |
c1 = 5       # 0101
c2 = 3       # 0011
print(c1 | c2)     # 7  (0111)
print("----------------")

# ^
d1 = 5       # 0101
d2 = 3       # 0011
print(d1 ^ d2)     # 6  (0110)
print("----------------")

# ~
e1 = 6        # 0000....0110
print(~e1)       # -7 (1111....1001)
print("----------------")

# Left Shift
f1 = 5        # 0000....0101
f2 = 2
print(f1 << f2)    # 20 (0000....10100)
print("----------------")

# Right Shift
g1 = 20       # 0000....10100
g2 = 2
print(g1 >> g2)    # 5  (0000....0101)
print("----------------")

g3 = 5      # 0000....0011
g4 = 2      
print(g3 >> g4)    # 0  (0000....0000)
print("---------------------------------------------------------------------")